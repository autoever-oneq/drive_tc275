char receiveCharFromShell(void) {
    uint8 buf[10] = { 0, };
    int bufSize = 1;
    IfxAsclin_Asc_read(uartInterfaces[0].asclin, buf, &bufSize, TIME_INFINITE);
    if (buf[0] > 0) {
        IfxAsclin_Asc_write(uartInterfaces[0].asclin, buf, &bufSize, TIME_INFINITE);
    }

    return buf[0];
}

void newLineShell(void) {
    int size = 2;
    IfxAsclin_Asc_write(uartInterfaces[0].asclin, "\r\n", &size, TIME_INFINITE);
}

void print(char* str, int len) {
    IfxAsclin_Asc_write(uartInterfaces[0].asclin, str, &len, TIME_INFINITE);
}

