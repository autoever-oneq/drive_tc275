/* Function to initialize ASCLIN module */
static void initSerialInterface(int uartNum)
{
    IfxAsclin_Asc_Config ascConf;

    /* Set default configurations */
    IfxAsclin_Asc_initModuleConfig(&ascConf, uartInterfaces[uartNum].module); /* Initialize the structure with default values      */

    /* Set the desired baud rate */
    ascConf.baudrate.baudrate = ASC_BAUDRATE;                                   /* Set the baud rate in bit/s       */
    ascConf.baudrate.oversampling = IfxAsclin_OversamplingFactor_16;            /* Set the oversampling factor      */

    /* Configure the sampling mode */
    ascConf.bitTiming.medianFilter = IfxAsclin_SamplesPerBit_three;             /* Set the number of samples per bit*/
    ascConf.bitTiming.samplePointPosition = IfxAsclin_SamplePointPosition_8;    /* Set the first sample position    */

    /* ISR priorities and interrupt target */
    ascConf.interrupt.txPriority = uartInterfaces[uartNum].txPriority;  /* Set the interrupt priority for TX events             */
    ascConf.interrupt.rxPriority = uartInterfaces[uartNum].rxPriority;  /* Set the interrupt priority for RX events             */
    ascConf.interrupt.typeOfService = IfxSrc_Tos_cpu0;

    /* Pin configuration */
    const IfxAsclin_Asc_Pins pins = {
            .cts        = NULL_PTR,                         /* CTS pin not used                                     */
            .ctsMode    = IfxPort_InputMode_pullUp,
            .rx         = uartInterfaces[uartNum].rxPin,        /* Select the pin for RX connected to the USB port      */
            .rxMode     = IfxPort_InputMode_pullUp,         /* RX pin                                               */
            .rts        = NULL_PTR,                         /* RTS pin not used                                     */
            .rtsMode    = IfxPort_OutputMode_pushPull,
            .tx         = uartInterfaces[uartNum].txPin,         /* Select the pin for TX connected to the USB port      */
            .txMode     = IfxPort_OutputMode_pushPull,      /* TX pin                                               */
            .pinDriver  = IfxPort_PadDriver_cmosAutomotiveSpeed1
    };
    ascConf.pins = &pins;

    /* FIFO buffers configuration */
    ascConf.txBuffer = uartInterfaces[uartNum].txBuffer;                      /* Set the transmission buffer                          */
    ascConf.txBufferSize = ASC_TX_BUFFER_SIZE;              /* Set the transmission buffer size                     */
    ascConf.rxBuffer = uartInterfaces[uartNum].rxBuffer;                      /* Set the receiving buffer                             */
    ascConf.rxBufferSize = ASC_RX_BUFFER_SIZE;              /* Set the receiving buffer size                        */

    /* Init ASCLIN module */
    IfxAsclin_Asc_initModule(uartInterfaces[uartNum].asclin, &ascConf);          /* Initialize the module with the given configuration   */
}

void initUartInterface(void) {
    for (int i = 0; i < 3; i++) {
        initSerialInterface(i);
    }
}

void sendUart(int uartNum, const void* data, Ifx_SizeT* dataSize) {
    if (uartNum < 0 || uartNum > 3) {
        int x = 5;
        IfxAsclin_Asc_write(uartInterfaces[0].asclin, "FUCK?", &x, TIME_INFINITE);
        return;
    }

    if (uartNum > 0) {
        IfxAsclin_Asc_write(uartInterfaces[uartNum].asclin, data, dataSize, TIME_INFINITE);
    }
}

void printUart(const void* data, Ifx_SizeT* dataSize) {
    uint8_t* data_byte = (uint8_t *)data;
    for (int i = 0; i < *dataSize; i++) {
        int cnt = 5;
        char byteToHex[5] = "0x";
        byteToHex[2] = convertDecToHexAscii(data_byte[i] >> 4);
        byteToHex[3] = convertDecToHexAscii(data_byte[i] & 0xF);
        byteToHex[4] = ' ';
        IfxAsclin_Asc_write(uartInterfaces[0].asclin, byteToHex, &cnt, TIME_INFINITE);
    }

    newLineShell();
}

static char convertDecToHexAscii(uint8_t x) {
    switch (x) {
        case 0:
        case 1:
        case 2:
        case 3:
        case 4:
        case 5:
        case 6:
        case 7:
        case 8:
        case 9:
            return x + '0';
        default:
            return x - 0xA + 'A';
    }
}

